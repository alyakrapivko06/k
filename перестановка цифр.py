abc = int(input()) # получаем трехзначное число и сохраняем в переменную abc
c = abc % 10 # последняя цифра числа
b = (abc % 100) // 10 # предпоследняя цифра числа
a = abc // 100 # первая цифра числа
print(a, b, c, sep= " ") # выводим перебор всех перестановок цифр
print(a, c, b, sep= " ")
print(b, a, c, sep= " ")
print(b, c, a, sep= " ")
print(c, a, b, sep= " ")
print(c, b, a, sep= " ")